var soma = 1 + 2; //oparador de audição

var subtracao = 10 - 5; // operador de subtracao

var divisao = 20 / 2; //operador de divisao

var multiplicacao = 2 * 2; //operador de multiplicação

var resto = 5 % 2; //operador de resto da divisao

soma++; //pos inclemento
++soma; //pre inclemento

soma--; //pos declemento
--soma; //pre declemento

soma += 20; // igual a soma + 20

soma -= 20; // igual a soma - 20

console.log(`soma de 1 + 2= ${soma}`);
console.log(`subtracao de 10 - 5= ${subtracao}`);
console.log(`divisao de 20 / 2= ${divisao}`);
console.log(`multiplicacao de 2 * 2= ${multiplicacao}`);
console.log(`resto da divisao de 5 % 2= ${resto}`);
